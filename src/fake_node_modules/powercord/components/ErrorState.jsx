const { React, getModule } = require('powercord/webpack');
const AsyncComponent = require('./AsyncComponent');
const Icon = require('./Icon');

module.exports = AsyncComponent.from((async () => {
  const { error, backgroundRed, icon, text } = await getModule([ 'error', 'backgroundRed' ]);
  const { marginBottom20 } = await getModule([ 'marginBottom20' ]);

  return (props) => (
    <div className={`${error} ${backgroundRed} ${marginBottom20}`}>
      <Icon className={icon} name='WarningCircle'/>
      <div className={text}>
        {props.children}
      </div>
    </div>
  );
})());
